// ─── Conversation tracker ─────────────────────────────────────────────────────
//
// Tracks the tool_use_id → LLM call linkage so consecutive turns in a conversation
// can be chained: LlmCall N (generates tool_uses) → tool execution → LlmCall N+1
// (sends tool_results referencing those ids → parentLlmCallId = N.id).
//
// Entries are evicted after 30 minutes (well beyond any realistic conversation turn).

interface ToolUseEntry {
  llmCallId: string;
  conversationId: string;
  expiresAt: number;
}

class ConversationTracker {
  private readonly toolUseMap = new Map<string, ToolUseEntry>();

  /** Register tool_use blocks generated by a completed LLM response. */
  register(toolUseIds: string[], llmCallId: string, conversationId: string): void {
    const expiresAt = Date.now() + 30 * 60_000;
    for (const id of toolUseIds) {
      this.toolUseMap.set(id, { llmCallId, conversationId, expiresAt });
    }
    this.evict();
  }

  /**
   * Look up the parent LLM call for a set of tool_use_ids referenced in a request.
   * Returns the first match found (all ids in a turn share the same parent call).
   * Also evicts expired entries so pure text-only conversations (no tool calls)
   * don't accumulate stale entries indefinitely.
   */
  resolve(toolUseIds: string[]): { llmCallId?: string; conversationId?: string } {
    this.evict();
    const now = Date.now();
    for (const id of toolUseIds) {
      const entry = this.toolUseMap.get(id);
      if (entry && entry.expiresAt > now) {
        return { llmCallId: entry.llmCallId, conversationId: entry.conversationId };
      }
    }
    return {};
  }

  private evict(): void {
    const now = Date.now();
    for (const [key, entry] of this.toolUseMap.entries()) {
      if (entry.expiresAt <= now) this.toolUseMap.delete(key);
    }
  }
}

